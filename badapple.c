static void draw_large_char(int x, int y, char c, int scale) {
    if (c < 32 || c > 126) c = '?';
    const uint8_t *g = font5x7[c - 32];
    for (int col = 0; col < 5; col++) {
        uint8_t bits = g[col];
        for (int row = 0; row < 7; row++) {
            if (bits & (1 << row)) {
                fill_rect(x + col * scale, y + row * scale, 
                          x + col * scale + scale - 1, y + row * scale + scale - 1);
            }
        }
    }
}

static void draw_large_str(int x, int y, const char *s, int scale) {
    while (*s) { draw_large_char(x, y, *s++, scale); x += (5 + 1) * scale; }
}

int main(int argc, char *argv[]) {
    int daemonize = 0;
    for (int i = 1; i < argc; i++) {
        if (strcmp(argv[i], "-d") == 0) {
            daemonize = 1;
        } else if (strcmp(argv[i], "-h") == 0 || strcmp(argv[i], "-?") == 0) {
            usage(argv[0]);
            return 0;
        } else {
            usage(argv[0]);
            return 1;
        }
    }
    if (daemonize) { if (daemon(0, 0) == -1) return 1; }

    i2c_fd = open("/dev/i2c-1", O_RDWR);
    if (i2c_fd < 0) { perror("i2c-1"); return 1; }
    if (ioctl(i2c_fd, I2C_SLAVE, ADDR) < 0) { perror("ioctl"); return 1; }
    init_display();

    /* Clear and prepare background headers */
    memset(fb, 0, 1024);
    draw_large_str(10, 1, "BAD APPLE!", 2);

    gzFile f = gzopen("bad_apple.bin.gz", "rb");
    if (!f) {
        printf("Error: Could not locate bad_apple.bin.gz payload chunk!\n");
        return 1;
    }
    
    long frame_ns = 1000000000L / 30; // 30 FPS target
    struct timespec next;
    clock_gettime(CLOCK_MONOTONIC, &next);
    
    /* Dedicated A/V Sync Engine bounding to true hardware time */
    while(gzread(f, fb + 256, 768) == 768) {
        struct timespec curr;
        clock_gettime(CLOCK_MONOTONIC, &curr);
        
        long lag_ns = (curr.tv_sec - next.tv_sec) * 1000000000L + (curr.tv_nsec - next.tv_nsec);
        
        /* Frame-skip logic: If the I2C bus pipeline is drastically lagging behind Real-Time (>1 frame), 
           we skip emitting this frame to the hardware entirely to catch up geometrically with the audio */
        if (lag_ns < frame_ns) {
            flush();
        }

        /* Calculate absolute bounds for the forthcoming frame execution target */
        next.tv_nsec += frame_ns;
        while (next.tv_nsec >= 1000000000L) {
            next.tv_nsec -= 1000000000L;
            next.tv_sec += 1;
        }
        
        /* If we are ahead of schedule, sleep precisely until the atomic deadline triggers */
        clock_gettime(CLOCK_MONOTONIC, &curr);
        long wait_ns = (next.tv_sec - curr.tv_sec) * 1000000000L + (next.tv_nsec - curr.tv_nsec);
        if (wait_ns > 0 && wait_ns < 1000000000L) {
            clock_nanosleep(CLOCK_MONOTONIC, TIMER_ABSTIME, &next, NULL);
        }
    }
    gzclose(f);
    
    memset(fb, 0, 1024); flush();
    uint8_t off[2] = {0x00, 0xAE};
    if (write(i2c_fd, off, 2) < 0) return 1;
    close(i2c_fd);
    
    return 0;
}
